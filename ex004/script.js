var idadePrimeiro = prompt("Digite sua primeira idade")
var idadeSegundo = prompt("Digite sua segunda idade")

if (idadePrimeiro > idadeSegundo){
    alert("Usuario 1 é o mais velho")
}
else if (idadePrimeiro == idadeSegundo){
    alert("Os usuarios tem a mesma idade")
}
else if (idadePrimeiro < idadeSegundo){
    alert("O segundo Usuario é o mais velho")
}